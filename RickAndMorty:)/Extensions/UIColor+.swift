import UIKit

extension UIColor {
    
    static func titleColor() -> UIColor {
        return #colorLiteral(red: 0.7381541398, green: 1, blue: 0.1184886116, alpha: 1)
    }
    
    static func backgroundColor() -> UIColor {
        return #colorLiteral(red: 0.09995814976, green: 0.1179966856, blue: 0.1634539676, alpha: 1)
    }
}
